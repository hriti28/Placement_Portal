						<h3><u><b>Interview process</b></u></h3>
						<p>With countless applicants vying for jobs, candidates are required to go through a three-stage selection process involving elimination after every round.<br>
						<b>Here’s all you need to know about the campus selection process at TCS:</b></p>
						<b><u><b>Aptitude Test</b></u></b><br>
						<p>It consists of four rounds:<br>
						   <li>Email writing (1 question, 10 minutes)</li>
						   <li> Quantitative ability and reasoning ability (20 questions, 40 minutes)</li>
						   <li> Programming language efficiency (10 questions, 20 minutes)</li>
 						   <li> Coding test (1 question, 20 minutes)</li></p>

						<p><b><u>Email Writing</u></b><br>
							You will be given instructions and a set of keywords. Follow the exact directions, and use all the keywords in the same order as listed in the question. Omit all unnecessary details from your email. Your answer should be a minimum of 50 words; the ideal length is 70-80 words.<br>
						Sometimes, the word length is mentioned in the question. Avoid spelling errors and typos, big words and complex sentences, keep the writing crisp and to the point, and remember to use accepted norms for salutation and signing off.</p>

						<p><b><u>Analytical Reasoning</u></b><br>
							This section tests your quantitative and reasoning ability. Time management is critical. Try and solve the easy questions first before attempting the difficult ones. There are certain ‘star’ questions that carry more marks; don’t get stuck on these unless you are confident of solving them quickly since it could upset your time management. Instead, return after solving the easy questions.</p>

						<p><b><u>Programming Language Efficiency </u></b><br>
							This is a new round introduced by TCS where candidates must answer 10 multiple choice questions on C language based on basic programming concepts.</p>

						<p><b><u>Coding Test </u></b><br>
							This test is of the Command Line Programming type where candidates are required to solve one programming question in 20 minutes in C language using an inbuilt online compiler. Prepare for basic programs like leap year GCD, Fibonacci series, HCF, LCM, odd/ even series, etc.</p>

						<p><b><u>Personal Interviews</u></b><br>
							This includes three rounds—technical, managerial and HR. The managerial round may be omitted depending on the discretion of the employers.</p>

						<p><b><u>Technical Round </u></b><br>
							Choose two domains or technologies as your specialization and study them in depth including new versions and updates. Cover the general programming concepts. Knowledge of C language is a plus. Be prepared for detailed questions on the projects you’ve worked on. You could also be asked to write programs during the interview.</p>

						<p><b><u>Managerial Round </u></b><br>
							This is a stress interview intended to gauge your response to a pressure situation. Expect brain teasers, deliberate efforts to confound you, skepticism, and emotional intimidation. Much depends on your interviewer.</p>

						<p><b><u>HR Round </u></b><br>
							This is where they assess things like your attitude and fitment in the company, apart from informing you about the terms and conditions of employment. Expect standard HR questions on why you want to join the company, why you think you’re a better choice than other candidates, your strengths and weaknesses, apart from behavioral questions.<br>
						In the interview rounds, remember that you are constantly being judged on your confidence, personality, and soft skills, so remain calm and make an effort to be pleasant even if you are under strain. For more information on handling the interview, click on this link.<br>
						While the recruitment process may seem quite rigorous, a few weeks of preparation could give you a good shot at getting selected. So prepare well and apply. It could be an ideal launch pad for your career!
